const LanguageContext = createContext({
  language: 'en',
  setLanguage: () => {},
  t: (key) => key,
})

const getInitialLanguage = () => {
  if (typeof window === 'undefined') return 'en'
  return localStorage.getItem(STORAGE_KEY) || 'en'
}

export const LanguageProvider = ({ children }) => {
  const [language, setLanguage] = useState(getInitialLanguage)

  useEffect(() => {
    if (typeof window === 'undefined') return
    localStorage.setItem(STORAGE_KEY, language)
  }, [language])

  const t = (key, params = {}) => {
    const dict = translations[language] || translations.en
    let text = dict[key] || translations.en[key] || key
    
    // æ”¯æŒå‚æ•°æ›¿æ¢ï¼Œä¾‹å¦‚ {countdown} ä¼šè¢«æ›¿æ¢ä¸ºå®é™…å€¼
    if (params && typeof text === 'string') {
      Object.keys(params).forEach(paramKey => {
        text = text.replace(new RegExp(`\\{${paramKey}\\}`, 'g'), params[paramKey])
      })
    }
    
    return text
  }

  // è·å–åˆ†ç±»åç§°çš„ç¿»è¯‘
  const getCategoryName = (categoryName) => {
    const key = `category.${categoryName}`
    const translated = t(key)
    // å¦‚æœç¿»è¯‘é”®ä¸å­˜åœ¨ï¼Œè¿”å›åŸå§‹åç§°
    return translated === key ? categoryName : translated
  }

  return (
    <LanguageContext.Provider value={{ language, setLanguage, t, getCategoryName }}>
      {children}
    </LanguageContext.Provider>
  )
}

export const useLanguage = () => useContext(LanguageContext)



